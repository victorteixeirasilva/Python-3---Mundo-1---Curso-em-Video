# Crie um algoritimo que leia um número e mostre o seu dobro, triplo e raiz quadrada

number = int(input('Informe um número '))
print('O dobro do valor informado é {}'.format(number*2))
print('O triplo do valor informado é {}'.format(number*3))
print('A raiz quadrada do valor informado é {}'.format(number**(1/2)))